/// Chart type enum
enum ChartType {
  /// Area chart type
  area,

  /// Candle chart type
  candle,
}

/// Chart interval enum
enum ChartInterval {
  /// 1 tick
  oneTick,

  /// 1 minute
  oneMin,

  /// 2 minutes
  twoMin,

  /// 3 minutes
  treeMin,

  /// 5 minutes
  fiveMin,

  /// 10 minutes
  tenMin,

  /// 15 minutes
  fifteenMin,

  /// 30 minutes
  thirtyMin,

  /// 1 hour
  oneHour,

  /// 2 hours
  twoHours,

  /// 4 hours
  fourHours,

  /// 8 hours
  eightHours,

  /// 1 day
  oneDay,
}
