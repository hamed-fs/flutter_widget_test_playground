/// Chart type enum
enum ChartType {
  /// Area chart type
  area,

  /// Candle chart type
  candle,
}

/// Chart interval enum
enum ChartInterval {
  /// 1 tick interval
  oneTick,

  /// 1 minute interval
  oneMinute,

  /// 2 minutes interval
  twoMinutes,

  /// 3 minutes interval
  treeMinutes,

  /// 5 minutes interval
  fiveMinutes,

  /// 10 minutes interval
  tenMinutes,

  /// 15 minutes interval
  fifteenMinutes,

  /// 30 minutes interval
  thirtyMinutes,

  /// 1 hour interval
  oneHour,

  /// 2 hours interval
  twoHours,

  /// 4 hours interval
  fourHours,

  /// 8 hours interval
  eightHours,

  /// 1 day interval
  oneDay,
}
