/// Chart type enum
enum ChartType {
  /// Area chart type
  area,

  /// Candle chart type
  candle,
}

/// Chart interval enum
enum ChartInterval {
  /// 1 tick
  oneTick,

  /// 1 minute
  oneMin,

  /// 2 minute
  twoMin,

  /// 3 minute
  treeMin,

  /// 5 minute
  fiveMin,

  /// 10 minute
  tenMin,

  /// 15 minute
  fifteenMin,

  /// 30 minute
  thirtyMin,

  /// 1 hour
  oneHour,

  /// 2 hours
  twoHours,

  /// 4 hours
  fourHours,

  /// 8 hours
  eightHours,

  /// 1 day
  oneDay,
}
